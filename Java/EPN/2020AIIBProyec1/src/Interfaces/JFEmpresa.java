    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Fecha fechEntrada = new Fecha(this.jTFechaIngreso.getText());
        Fecha fechSalida = new Fecha(this.jTFSalida.getText());
        
        empleado = new Empleado(fechEntrada, fechSalida, this.jTFNombre.getText());
        this.jTAResultado1.setText(empleado.toString());
        listaEmpleados.setEmpleados(empleado);
        lista = listaEmpleados.toString();
        this.jTAResultado2.setText(lista);
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

