    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        this.jTFDimension.requestFocus();
        if(nuevoValor == ""){
            try
            {
                vector = new Vector(Integer.parseInt(this.jTFDimension.getText()));
                vector.setVector();   
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"Solo números enteros porfavor");
                this.jTFDimension.setText("");
                this.jTFDimension.requestFocus();    
            }     
        }
        
        salida += "\n" + vector.toString();
        this.jTAResultado.setText(salida);
        this.jCBOperaciones.setVisible(true);
    }//GEN-LAST:event_jBAceptarActionPerformed

