    private void jCBOperacionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBOperacionesActionPerformed
        switch(this.jCBOperaciones.getSelectedIndex())
        {
            case 0:
            {
                salida += "\n Suma de Elementos: " + vector.sumarElementos();
                nuevoValor = "valor antiguo";
                
                break;
            }
            case 1:
            {
                salida+= "\n Suma de Primos: " + vector.sumaPrimos();
                nuevoValor = "valor antiguo";
                break;
            }
            case 2:
            {
                salida+= "\n En total estan: " + vector.contarNumPerfecto()+ " numeros "
                        + " perfectos";
                nuevoValor = "valor antiguo";
                break;
                
            }
            case 3:
            {
                salida+= "\n Se ha clasidicado el vector de mayor a menor: ";
                vector.clasificarVector();
                nuevoValor = "valor antiguo";
                break;
                
            }
            case 4:  
            {
                salida+= "\n El mayor número es: " + vector.numMayor();
                nuevoValor = "valor antiguo";
                break;
            }
            case 5:
            {
                salida+= "\n El menor número es: " + vector.numMenor();
                nuevoValor = "valor antiguo";
                break;
            }
            case 6:
            {
                this.jTFDimension.setVisible(false);
                this.jLDimension.setVisible(false);
                this.jBAceptar.setVisible(false);
                this.jBNuevo.setVisible(false);
                this.jCBOperaciones.setVisible(false);
                this.jBIngresar.setVisible(true);
                this.jLIngresarV.setVisible(true);
                this.jTFBuscar.setVisible(true);
                break;
            }
        }
    }//GEN-LAST:event_jCBOperacionesActionPerformed

    private void jBIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBIngresarActionPerformed
        try{
            
           int a = Integer.parseInt(this.jTFBuscar.getText());
           int indice = vector.busquedaBinaria(a);
           if(indice >= 0){
                salida += "\n El valor que buscas para el ordenado  vector: " + vector
                        + "\n probablemente se encuentre en "
                        + "la posicion: " + indice;
           }else{
                salida += "\n El valor que buscas para el ordenado  vector: " + vector
                        + "\n no se a encontrado ";
                        
           }
           this.jTAResultado.setText(salida);
           this.jTFDimension.setVisible(true);
           this.jLDimension.setVisible(true);
           this.jBAceptar.setVisible(true);
           this.jBNuevo.setVisible(true);
           this.jCBOperaciones.setVisible(true);
           this.jBIngresar.setVisible(false);
           this.jLIngresarV.setVisible(false);
           this.jTFBuscar.setVisible(false);
           
           
            
        }catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "ingrese un número");
            this.jTFBuscar.setText("");
            this.jTFBuscar.requestFocus();
        }
    }//GEN-LAST:event_jBIngresarActionPerformed

